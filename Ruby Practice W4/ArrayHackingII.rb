arr2 = ["one", "two", "three"]
p arr2[0]     # Gets position 0, or the first in the list

p arr2[2]      # Gets the string "Three"

p arr2[1,2]    # "two" and "three"

p arr2[5]       # nil: when theres uncertainty, Ruby tends to give nil

p arr2[2] + arr2[0]   # ["threeone"] : single word

p arr2.first    # position 0
p arr2.last      # position 2: three





